public class IntegerDivision {
    public static void main(String[] args) {
        // 宣告並賦值兩個整數變數
        int a = 10;
        int b = 5;

        // 計算商（整數除法）
        int quotient = a / b;

        // 輸出結果
        System.out.println("a 的值為: " + a);
        System.out.println("b 的值為: " + b);
        System.out.println("a 除以 b 的商為: " + quotient);
    }
}
